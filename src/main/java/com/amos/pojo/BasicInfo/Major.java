package com.amos.pojo.BasicInfo;

/**
 * @author Amos
 */
public class Major {
    private long mid;
    private String maname;
    private Long seid;

    public long getMid() {
        return mid;
    }

    public void setMid(long mid) {
        this.mid = mid;
    }

    public String getManame() {
        return maname;
    }

    public void setManame(String maname) {
        this.maname = maname;
    }

    public Long getSeid() {
        return seid;
    }

    public void setSeid(Long seid) {
        this.seid = seid;
    }
}
